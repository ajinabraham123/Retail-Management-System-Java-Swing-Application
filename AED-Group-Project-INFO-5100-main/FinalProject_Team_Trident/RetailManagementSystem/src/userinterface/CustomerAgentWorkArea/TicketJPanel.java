    public void populateWorkRequestTable() {

        DefaultTableModel model = (DefaultTableModel) ticketJTable.getModel();

        model.setRowCount(0);

        for (TicketList prescription : customer.getTicketlist().getPrescriptionList()) {
           
                Object[] row = new Object[5];
                row[0] = prescription.getTypeName();

                row[1] = prescription.getNoofTimesInaday();
                row[2] = prescription.getTotalDays();         
                model.addRow(row);
        }

    }
