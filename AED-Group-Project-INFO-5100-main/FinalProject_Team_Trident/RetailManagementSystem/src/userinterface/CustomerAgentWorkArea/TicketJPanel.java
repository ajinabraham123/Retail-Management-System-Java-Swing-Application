    public void saveRecord(String network,String disease,String medicine,int times, int days){
        try {
            FileWriter file = new FileWriter(filePath,true);
            BufferedWriter bw = new BufferedWriter(file);
            PrintWriter pw = new PrintWriter(bw);

            pw.println("Product Type - " + medicine+", Qty of Product "+times + ", Expiry Days Left"+days);
            pw.flush();
            pw.close();
            log.debug("saving ticket details to ticket data.txt");
        } catch (IOException ex) {
            java.util.logging.Logger.getLogger(TicketJPanel.class.getName()).log(Level.SEVERE, null, ex);
            log.error("ticket data.txt" +ex);

        }
        
    }
    
